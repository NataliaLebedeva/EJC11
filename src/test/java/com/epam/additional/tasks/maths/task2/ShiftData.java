package com.epam.additional.tasks.maths.task2;

public class ShiftData {
    private int shift;
    private String source;

    public ShiftData(int shift, String source) {
        this.shift = shift;
        this.source = source;
    }

    public int getShift() {
        return shift;
    }

    public String getSource() {
        return source;
    }
}
